const locales = ['en', 'zh', 'ja', 'ko', 'es', 'ru'];
const dir = '/Users/sgx/Desktop/Social/Dev/emojikit/src/i18n/dictionaries';

for (const locale of locales) {
  const filePath = path.join(dir, `${locale}.json`);
  let data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  
  if (!data.categories) {
    data.categories = {};
  }
  
  // Apply translations safely, replace special chars if any but let's just use original english string as key
  for (const [enKey, localizedVal] of Object.entries(translations[locale])) {
    data.categories[enKey] = localizedVal;
  }
  
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2));
  console.log(`Updated ${locale}.json`);
}

